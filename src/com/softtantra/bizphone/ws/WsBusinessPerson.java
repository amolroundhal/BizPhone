package com.softtantra.bizphone.ws;

public class WsBusinessPerson extends WsLogin{

	 String first_name;
	 String last_name;
	 String email;
	 String mobile_no;
	 String business_password;
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getMobile_no() {
		return mobile_no;
	}
	public void setMobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}
	public String getBusiness_password() {
		return business_password;
	}
	public void setBusiness_password(String business_password) {
		this.business_password = business_password;
	}
	
	 
}
	 
